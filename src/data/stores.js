// 매장 정보 가져오기 (ID 기준)
export const getStoreById = (id) => {
  return DAISO_STORES.find(store => store.id === id);
};

// 매장 정보 가져오기 (매장명 기준)
export const getStoreByName = (name) => {
  return DAISO_STORES.find(store => store.name === name);
};

// 구별 매장 목록 가져오기
export const getStoresByDistrict = (district) => {
  return DAISO_STORES.filter(store => store.district === district);
};

// 전체 매장 수
export const getTotalStoreCount = () => {
  return DAISO_STORES.length;
};

// 구별 통계
export const getDistrictStats = () => {
  const stats = {};
  DAISO_STORES.forEach(store => {
    if (store.district) {
      stats[store.district] = (stats[store.district] || 0) + 1;
    }
  });
  return stats;
};
